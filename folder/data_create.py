def input_fname():
    fnam = str(input("Введите имя: ")).capitalize()
    return fnam

def input_lname():
    lnam = str(input("Введите фамилию: ")).capitalize()
    return lnam

def input_telnum():
    tel_nam = str(input("Введите номер телефона: "))
    return tel_nam

def input_address():
    adr = str(input("Введите адрес: ")).title()
    return adr